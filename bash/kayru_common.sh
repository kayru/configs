# make ls nicer

UNAME=`uname`

if [ "$UNAME" == "Darwin" ]; then
    export TERM=xterm-color
    alias ls='ls -G'
else
    alias ls='ls --color=auto'    
fi

alias l='ls -CF'
alias la='ls -A'
alias ll='ls -alF'

